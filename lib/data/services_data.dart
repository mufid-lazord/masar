  static List<Service> search(String query) {
    if (query.isEmpty) return allServices;
    final lowerQuery = query.toLowerCase();
    return allServices.where((s) =>
        s.name.toLowerCase().contains(lowerQuery) ||
        s.category.toLowerCase().contains(lowerQuery) ||
        s.address.toLowerCase().contains(lowerQuery) ||
        s.description.toLowerCase().contains(lowerQuery)).toList();
  }
